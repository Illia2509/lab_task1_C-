#include <iostream>  // Підключення бібліотеки для введення та виведення
using namespace std;  // Використання простору імен std

int main() {
    int n;  // Оголошення змінної n для збереження розміру матриці
    cin >> n;  // Зчитування розміру матриці з клавіатури

    int a[n][n];  // Оголошення двовимірного масиву розміром n x n
    for(int i = 0; i < n; i++) {  // Цикл для рядків матриці
        for(int j = 0; j < n; j++) {  // Цикл для стовпців матриці
            cin >> a[i][j];  // Зчитування елемента матриці з клавіатури
        }
    }

    int sum = 0;  // Ініціалізація змінної sum для зберігання суми діагональних елементів
    for(int i = 0; i < n; i++) {  // Цикл по головній діагоналі
        sum += a[i][i];  // Додавання елемента на головній діагоналі до суми
    }

    cout << "Sum of diagonal elements: " << sum << endl;  // Виведення результату на екран
    return 0;  // Завершення програми
}